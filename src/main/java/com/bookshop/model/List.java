package com.bookshop.model;


public class List {

  private long listid;
  private long bookid;
  private long listquantity;


  public long getListid() {
    return listid;
  }

  public void setListid(long listid) {
    this.listid = listid;
  }


  public long getBookid() {
    return bookid;
  }

  public void setBookid(long bookid) {
    this.bookid = bookid;
  }


  public long getListquantity() {
    return listquantity;
  }

  public void setListquantity(long listquantity) {
    this.listquantity = listquantity;
  }

}
